
package com.velotech.fanselection.dtos;

public class DropdownlistDto {

	private int id;

	private String description;

	private String value;

	private String whereUsed;

	private String changeAllowed;

	public int getId() {

		return id;
	}

	public void setId(int id) {

		this.id = id;
	}

	public String getDescription() {

		return description;
	}

	public void setDescription(String description) {

		this.description = description;
	}

	public String getValue() {

		return value;
	}

	public void setValue(String value) {

		this.value = value;
	}

	public String getWhereUsed() {

		return whereUsed;
	}

	public void setWhereUsed(String whereUsed) {

		this.whereUsed = whereUsed;
	}

	public String getChangeAllowed() {

		return changeAllowed;
	}

	public void setChangeAllowed(String changeAllowed) {

		this.changeAllowed = changeAllowed;
	}
}
