
package com.velotech.fanselection.dtos;

public class NozzleDataDto {

	private int nozzleDataId;

	private Integer nozzleMasterId;

	private String modelMaster;

	private String parameter;

	private String value;

	public int getNozzleDataId() {

		return nozzleDataId;
	}

	public void setNozzleDataId(int nozzleDataId) {

		this.nozzleDataId = nozzleDataId;
	}

	public String getModelMaster() {

		return modelMaster;
	}

	public void setModelMaster(String modelMaster) {

		this.modelMaster = modelMaster;
	}

	public Integer getNozzleMasterId() {

		return nozzleMasterId;
	}

	public void setNozzleMasterId(Integer nozzleMasterId) {

		this.nozzleMasterId = nozzleMasterId;
	}

	public String getParameter() {

		return parameter;
	}

	public void setParameter(String parameter) {

		this.parameter = parameter;
	}

	public String getValue() {

		return value;
	}

	public void setValue(String value) {

		this.value = value;
	}

}
