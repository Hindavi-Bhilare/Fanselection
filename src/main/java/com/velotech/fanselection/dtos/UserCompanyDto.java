
package com.velotech.fanselection.dtos;

public class UserCompanyDto {

	private int id;

	private String usermasterloginId;

	private Integer companyMasterId;

	private String companyName;

	private Boolean defaultComp;

	public int getId() {

		return id;
	}

	public void setId(int id) {

		this.id = id;
	}

	public String getCompanyName() {

		return companyName;
	}

	public void setCompanyName(String companyName) {

		this.companyName = companyName;
	}

	public String getUsermasterloginId() {

		return usermasterloginId;
	}

	public void setUsermasterloginId(String usermasterloginId) {

		this.usermasterloginId = usermasterloginId;
	}

	public Boolean getDefaultComp() {

		return defaultComp;
	}

	public void setDefaultComp(Boolean defaultComp) {

		this.defaultComp = defaultComp;
	}

	public Integer getCompanyMasterId() {

		return companyMasterId;
	}

	public void setCompanyMasterId(Integer companyMasterId) {

		this.companyMasterId = companyMasterId;
	}
}
