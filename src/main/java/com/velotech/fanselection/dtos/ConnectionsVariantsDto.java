
package com.velotech.fanselection.dtos;

import java.util.List;

public class ConnectionsVariantsDto {

	private int id;

	private List<Integer> variantMasterIds;

	private Integer variantId;

	private String variant;

	private Integer connectionId;

	public int getId() {

		return id;
	}

	public void setId(int id) {

		this.id = id;
	}

	public Integer getVariantId() {

		return variantId;
	}

	public void setVariantId(Integer variantId) {

		this.variantId = variantId;
	}

	public String getVariant() {

		return variant;
	}

	public void setVariant(String variant) {

		this.variant = variant;
	}

	public Integer getConnectionId() {

		return connectionId;
	}

	public void setConnectionId(Integer connectionId) {

		this.connectionId = connectionId;
	}

	public List<Integer> getVariantMasterIds() {

		return variantMasterIds;
	}

	public void setVariantMasterIds(List<Integer> variantMasterIds) {

		this.variantMasterIds = variantMasterIds;
	}

}
